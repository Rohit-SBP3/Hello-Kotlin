fun main(){


    // Simple Arithmetic Operators( +, -, /, *, %)
    // Relational Operators( >, <, <=, >=, !=, ==)
    // Increment and Decrement( i++, ++i, i--, --i) (Post and Pre)
    // Logical Operators( &&, ||, !)
    // NOTE: Short Circuiting (checks only one condition) &&(1st false then out) ||(first true then out)

    var a = 10;
    var b = 3;

    println(a/b)
    println(a.toFloat()/b)
    println(a++ + ++a - b-- - --b)
    print(a == b)
}